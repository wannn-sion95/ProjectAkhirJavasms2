package KodeProgram;

public class Buku {
    public String judul;
    public String penulis;
    public String genre;
    public String GambarBuku;
    public String content;

    public Buku(String judul, String penulis, String genre, String GambarBuku, String content) {
        this.judul = judul;
        this.penulis = penulis;
        this.genre = genre;
        this.content = content;
        this.GambarBuku = GambarBuku;
    }
}